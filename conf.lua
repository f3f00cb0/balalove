function love.conf(t)
    t.window.width = 1280
    t.window.height = 720
    t.window.title = "Balatro-Like Card Game"
    t.modules.joystick = false -- Disable joystick module if not used
    t.modules.physics = false -- Disable physics module if not used
    t.modules.touch = false   -- Disable touch module if not used
end
